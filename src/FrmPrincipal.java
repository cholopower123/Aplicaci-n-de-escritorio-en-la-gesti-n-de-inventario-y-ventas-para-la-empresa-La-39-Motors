    private void filtrarPorCategoria() {
    String categoriaSeleccionada = (String) cboxCategoria.getSelectedItem();
    if (categoriaSeleccionada == null || categoriaSeleccionada.equals("---SELECCIONA---")) {
        mostrarInventario(); // Muestra todo si no hay categoría seleccionada
        return;
    }

    DefaultTableModel modelo = (DefaultTableModel) tablaInventario.getModel();
    modelo.setRowCount(0); // Limpiar tabla

    List<Producto> productos = inventario.getListaProductos();
    for (Producto p : productos) {
        if (categoriaSeleccionada.equalsIgnoreCase(p.getCategoria())) {
            Object[] fila = {
                p.getNombre(),
                p.getPrecio(),
                p.getCantidad(),
                p.getCategoria(),
                p.getDescripcion(),
                p.getFecha() != null ? p.getFecha().toString() : ""
            };
            modelo.addRow(fila);
        }
    }
}

    
    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        int filaSeleccionada=tablaInventario.getSelectedRow();

        if(filaSeleccionada== -1){
            JOptionPane.showMessageDialog(this, "Seleccione el producto que desea editar");
            return;
        }
        Producto producto = inventario.getListaProductos().get(filaSeleccionada);

        String nuevoNombre = JOptionPane.showInputDialog(this, "Nuevo nombre:", producto.getNombre());
        String nuevoPrecioStr = JOptionPane.showInputDialog(this, "Nuevo precio:", producto.getPrecio());
        String nuevaCantidadStr = JOptionPane.showInputDialog(this, "Nueva cantidad:", producto.getCantidad());
        String nuevaDescripcion = JOptionPane.showInputDialog(this, "Nueva descripcion:", producto.getDescripcion());

        if (nuevoNombre != null && !nuevoNombre.trim().isEmpty() &&
    nuevoPrecioStr != null && !nuevoPrecioStr.trim().isEmpty() &&
    nuevaCantidadStr != null && !nuevaCantidadStr.trim().isEmpty() &&
    nuevaDescripcion != null && !nuevaDescripcion.trim().isEmpty()) {
            try {
                double nuevoPrecio = Double.parseDouble(nuevoPrecioStr);
                int nuevaCantidad = Integer.parseInt(nuevaCantidadStr);

                producto.setNombre(nuevoNombre);
                producto.setPrecio(nuevoPrecio);
                producto.setCantidad(nuevaCantidad);
                producto.setDescripcion(nuevaDescripcion);
                producto.setFecha(LocalDateTime.now());
                

                mostrarInventario();
                JOptionPane.showMessageDialog(this, "Producto actualizado correctamente.");
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Precio o cantidad inválidos.");
            }
        }else {
    JOptionPane.showMessageDialog(this, "Todos los campos deben ser completados ,NO SE PERMITEN VALORES NULOS");
}
    }//GEN-LAST:event_btnEditarActionPerformed

