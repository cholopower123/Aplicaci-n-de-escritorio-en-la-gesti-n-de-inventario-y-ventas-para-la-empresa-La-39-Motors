    private void actualizarComboCategoria(){
        cboxCategoria.removeAllItems();
        cboxCategoria.addItem("---SELECCIONA---");
        for(Categoria c: inventario.getListaCategorias()){
            cboxCategoria.addItem(c.getNombre());
        }
    }
    
