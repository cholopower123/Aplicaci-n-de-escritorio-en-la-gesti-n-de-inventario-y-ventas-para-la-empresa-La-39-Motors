    private void mostrarInventario(){
        DefaultTableModel modelo = (DefaultTableModel) tablaInventario.getModel();
        modelo.setRowCount(0); // Limpia filas previas
        List<Producto> productos = inventario.getListaProductos();
        
        for (Producto p : productos) {
        Object[] fila = {p.getNombre(), p.getPrecio(), p.getCantidad(),p.getCategoria(),p.getTipoProducto(), p.getDescripcion(),p.getFecha()!=null ? p.getFecha().toString():""};
        modelo.addRow(fila);
        }
   }
    
   
     
    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        String nombre = txtNombre.getText();
        String precio = txtPrecio.getText();
        String cantidad = txtCantidad.getText();
        String categoria = (String) cboxCategoria.getSelectedItem();
        String descripcion= txtdescripcion.getText();
        String tipoProducto = (String) cboxTipoProducto.getSelectedItem();
        
       LocalDateTime fechaActual = LocalDateTime.now();
       
       if (tipoProducto == null || tipoProducto.equals("---SELECCIONA---")) {
            JOptionPane.showMessageDialog(this, "Selecciona si es Producto o Repuesto.");
            return;
        }
       if (categoria == null || categoria.equals("---SELECCIONA---")) {
            JOptionPane.showMessageDialog(this, "Seleccione la categoria.");
            return;
        }
       
        boolean registrado = inventario.agregarProducto(nombre, precio, cantidad, categoria, descripcion, tipoProducto, fechaActual);
        if (registrado) {
            txtNombre.setText("");
            txtPrecio.setText("");
            txtCantidad.setText("");
            txtdescripcion.setText("");
            cboxCategoria.setSelectedIndex(0);
            cboxTipoProducto.setSelectedIndex(0);
            mostrarInventario(); 
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed
    
