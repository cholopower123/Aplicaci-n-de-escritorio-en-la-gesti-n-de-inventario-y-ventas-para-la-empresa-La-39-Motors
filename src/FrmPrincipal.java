    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
     int filaSeleccionada = tablaInventario.getSelectedRow();

if (filaSeleccionada >= 0) {
    // Obtener el producto seleccionado (opcional, si deseas mostrar nombre en el mensaje de confirmación)
    Producto productoSeleccionado = inventario.getListaProductos().get(filaSeleccionada);
    
    // Confirmación antes de eliminar
    int confirmacion = javax.swing.JOptionPane.showConfirmDialog(
        this,
        "¿Estás seguro de que deseas eliminar el producto: " + productoSeleccionado.getNombre() + "?",
        "Confirmar eliminación",
        javax.swing.JOptionPane.YES_NO_OPTION
    );

    if (confirmacion == javax.swing.JOptionPane.YES_OPTION) {
        // Eliminar del inventario
        inventario.getListaProductos().remove(filaSeleccionada);

        // Eliminar del modelo de la tabla
        DefaultTableModel modelo = (DefaultTableModel) tablaInventario.getModel();
        modelo.removeRow(filaSeleccionada);

        // Mensaje de éxito
        javax.swing.JOptionPane.showMessageDialog(this, "Producto eliminado con éxito.");
    }
} else {
    javax.swing.JOptionPane.showMessageDialog(this, "Seleccione una fila para eliminar.");
}

    }//GEN-LAST:event_btnEliminarActionPerformed

